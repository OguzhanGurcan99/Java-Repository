package java_lectures;
public class ForEachLoop {

	public static void main(String[] args) {
		
		char [] harfler = {'O','G','U','Z','H','A','N'};
		for (char harf : harfler) {
			System.out.print(harf);
		}
		
		System.out.println();
		
		int[] rakamlar = {1,2,3};
		for (int i : rakamlar) {
			System.out.println(i);
		}		
	}
}
